package flow.mvc.vo;

public class StockLogVO {

	private int sl_num, sl_code, sl_price, sl_qty;
	private String sl_id, sl_stock, sl_type, sl_time;
	
	
	public int getSl_num() {
		return sl_num;
	}
	public void setSl_num(int sl_num) {
		this.sl_num = sl_num;
	}
	public int getSl_code() {
		return sl_code;
	}
	public void setSl_code(int sl_code) {
		this.sl_code = sl_code;
	}
	public int getSl_price() {
		return sl_price;
	}
	public void setSl_price(int sl_price) {
		this.sl_price = sl_price;
	}
	public int getSl_qty() {
		return sl_qty;
	}
	public void setSl_qty(int sl_qty) {
		this.sl_qty = sl_qty;
	}
	public String getSl_id() {
		return sl_id;
	}
	public void setSl_id(String sl_id) {
		this.sl_id = sl_id;
	}
	public String getSl_stock() {
		return sl_stock;
	}
	public void setSl_stock(String sl_stock) {
		this.sl_stock = sl_stock;
	}
	public String getSl_type() {
		return sl_type;
	}
	public void setSl_type(String sl_type) {
		this.sl_type = sl_type;
	}
	public String getSl_time() {
		return sl_time;
	}
	public void setSl_time(String sl_time) {
		this.sl_time = sl_time;
	}
	
	
	
}
