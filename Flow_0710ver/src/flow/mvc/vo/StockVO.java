package flow.mvc.vo;

public class StockVO {

	private int s_code, s_open, s_high, s_low, s_close, s_volume;
	private String s_date;
	
	
	public int getS_code() {
		return s_code;
	}
	public void setS_code(int s_code) {
		this.s_code = s_code;
	}
	public int getS_open() {
		return s_open;
	}
	public void setS_open(int s_open) {
		this.s_open = s_open;
	}
	public int getS_high() {
		return s_high;
	}
	public void setS_high(int s_high) {
		this.s_high = s_high;
	}
	public int getS_low() {
		return s_low;
	}
	public void setS_low(int s_low) {
		this.s_low = s_low;
	}
	public int getS_close() {
		return s_close;
	}
	public void setS_close(int s_close) {
		this.s_close = s_close;
	}
	public int getS_volume() {
		return s_volume;
	}
	public void setS_volume(int s_volume) {
		this.s_volume = s_volume;
	}
	public String getS_date() {
		return s_date;
	}
	public void setS_date(String s_date) {
		this.s_date = s_date;
	}
	
	
	
	
}
